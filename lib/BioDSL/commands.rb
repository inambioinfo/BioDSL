module BioDSL
  # Module that require all files in the BioDSL/commands/ directory
  module Commands
    Dir[File.join(File.dirname(__FILE__), 'commands', '*')].each do |file|
      require file.split(File::SEPARATOR)[-3..-1].join(File::SEPARATOR).
        chomp('.rb')
    end
  end
end
